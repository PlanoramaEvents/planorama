<template>
  <div class="container-fluid">
    <div class="row">
      <div class="col-12">
        <b-form-group
          label="Notes"
          label-for="session-notes"
        >
          <b-form-textarea
            id="session-notes"
            v-model="session.item_notes"
            rows="10"
            @blur="saveSession()"
          ></b-form-textarea>
        </b-form-group>
      </div>
    </div>
  </div>
</template>

<script>
import { sessionModel } from '@/store/session.store'
import modelUtilsMixin from "@/store/model_utils.mixin";

export default {
  name: "SessionNotes",
  mixins: [
    modelUtilsMixin
  ],
  computed: {
    session() {
      return this.selected_model(sessionModel)
    }
  },
  methods: {
    saveSession() {
      this.save_model(sessionModel, this.session).then(
        (obj) => {
          this.$emit('input',obj)
        }
      )
    }
  }
}
</script>
